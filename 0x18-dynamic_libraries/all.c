#include <stdio.h>

/**
 * add - adds two integers
 * @a: first int
 * @b: second int
 * Return: Addition
 */
int add(int a,int b)
{
	return (a + b);
}
/**
 * sub - subtracts two integers
 * @a: first int
 * @b: second int
 * Return: Subtraction
 */
int sub(int a,int b)
{
	return (a - b);
}
/**
 * div - divides two integers
 * @a: first int
 * @b: second int
 * Return: Division
 */
int div(int a,int b)
{
	return (a / b);
}
/**
 * mul - multiplies two integers
 * @a: first int
 * @b: second int
 * Return: Multiplication
 */
int mul(int a,int b)
{
	return (a * b);
}
/**
 * mod - mod of two integers
 * @a: first int
 * @b: second int
 * Return: Answer
 */
int mod(int a,int b)
{
	return (a % b);
}
